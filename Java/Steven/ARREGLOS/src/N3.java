public class N3 {
    public static void main(String[] args) {
        int num1, num2;
        int sum, res, mul, div;
        num1=20;
        num2=10;
        sum=num1+num2;
        res=num1-num2;
        mul=num1*num2; 
        div=num1/num2;
        System.out.println(num1 + " + " + num2 + " -" + sum);
        System.out.println(num1 + " -" + num2 + " -" + res);
        System.out.println(num1 + " * " + num2 + " -" + mul);
        System.out.println(num1 + " /  " + num2 + " -" + div);
    }
}